public class SwitchProgram {
    public static void main(String[] args) {
        int day=9;
        switch (day) {
            case 1:
                System.out.println("sunday");
                break;
                case 2:
                System.out.println("monday");
                break;
              
            default:
            System.out.println("tuesday - saturday");
                break;
        }
    }
}
